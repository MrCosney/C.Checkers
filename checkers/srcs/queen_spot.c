int		queen_spot(char board[][10], int *pos)
{
	int x;
	int y;

	y = 1;
	while (y < 9)
	{
		x = 1;
		while (x < 9)
		{
			if (board[x][y] == 'W')
			{
				board[x][y] = '#';
				pos[0] = x;
				pos[1] = y;
				return (1);
			}
			x++;
		}
		y++;
	}
	return (0);
}
