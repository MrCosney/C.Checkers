int		*white_spot(char board[][10], int *pos)
{
	int x;
	int y;

	x = 10;
	while (x > 0)
	{
		y = 0;
		while (y < 10)
		{
			if (board[x][y] == 'w')
			{
				board[x][y] = '#';
				pos[0] = x;
				pos[1] = y;
				return (pos);
			}
			y++;
		}
		x--;
	}
	pos[0] = -1;
	return (0);
}
