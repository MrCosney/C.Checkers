void		board_array(char **argv, char board[][10])
{
	int l;
	int s;
	int x;

	x = 0;
	while (x <= 9)
	{
		board[0][x] = '#';
		board[9][x] = '#';
		x++;
	}
	l = 1;
	while (l <= 8)
	{
		s = 1;
		x = 0;
		while (x < 8)
		{
			board[l][0] = '#';
			board[l][s] = argv[l][x];
			s++;
			x++;
		}
		board[l][9] = '#';
		l++;
	}
}